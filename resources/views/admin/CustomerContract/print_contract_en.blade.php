            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (a) Master Driver and Services (Thailand) Co., Ltd., having the office located at 2222/9 Ladprao Road, Phlabphla
            Sub-district, Wangthonglang District, Bangkok 10130, hereinafter referred to as the "Contractor" of one part; and <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (b) KTBST Holding Public Company Limited holds
            having the office located at 87/2 CRC Tower, All Seasons Place, 9th, 18th, 39th and 52nd Floor,
            Wireless Road, Lumpini, Pathumwan, Bangkok 10330, THAILAND <br>
            (hereinafter referred to as the "Employer") of another part. <br>
            Both parties agree to enter into the Agreement with the following terms and conditions: <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <u> Clause 1. Service and Ierm of Agreement </u> <br>
            The Employer agrees to engage and the Contractor agrees to provide driver service for 1 driver(s) to the
            Employer, as per details shown in the attachment of the Agreement, hereinafter referred to as the "Driver", <br>
            The term of this Agreement is commencing from 08/01/2020 until 07/01/2021 <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <u> Clause 2. Date / time of service </u> <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 2.1 The Contractor shall provide the Driver to provide services to the Employer on the business day of the
            Employer : Monday to Friday, from 8.30 a.m. - 5.30 p.m. The service shall not be provided on Saturday and Sunday of
            every week and public holidays. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 2.2 If the Employer wishes service to be provided beside the day and/or time specified under Clause 2.1, the
            Employer shall notify the Contractor in advance and agree to pay additional service fee, as per condition specified in
            the attachment of this agreement. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <u> Clause 3. Service Fee </u> <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 3.1 The Employer agrees to pay the monthly service fee to the Contractor at the rate of 14,500 Baht (Fourteen thousand five hundred baht) per month, excluding VAT. The Employer shall
            pay the frst service fee from the date that this Agreement has commenced onwards. <br>
            For the next service fee and the additional service fee of the preceding month due to additional service
            beside the day/time of service as provided under Clause 2.2 or other reason, the Employer shall pay it monthly by the 30 of the calendar month. The Contractor shall calculate the additional service fee according to the
            rate provided in the attachment of this Agreement and the Employer shall pay such additional service fee without anyobjection. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 3.2 The Employer agrees and consents that the Contractor is able to increase the service fee under Clause 3.1 if
            the increase of the minimum wage is announced by the government. The service fee increased shall be according to
            the difference of the wage increased and the applicable wage before the announcement by referring to the minimum
            wage for Bangkok area. This shall also apply to the increase of the additional service fee under Clause 2.2 according
            to the proportion of the increase of service fee due to the increase of the minimum wage, If the service fee has to be
            adjusted under this Clause, the Contractor shall notify the Employer in writing at least 15 days prior to such increase. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 3.3 The Employer shall make payment under this Agreement by transferring the funds to the Contractor's account No. 200-088922-6,
            account name Master Driver &amp; Services (Thailand) Co., Ltd. Bank Bangkok Bank Public Ltd Branch Suanphlu or by post date cheque spacifying
            "Account Payee Only" payable to the Contractor or by cashier cheque or by cash at the office of the Contractor or by
            other method determined by the Contractor. <br>
            As each payment shall be considered material, in the event that the Employer fails to pay the service fee,
            the Contractor shall send the notice demanding the Employer to pay ail service fees within the date specified. The
            Employer agrees to pay interest at the rate of 1.25% per month of the unpaid service, calculating from the date of de-
            fault until the date that the Employer makes payment to the Contractor in full. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <u> Clause 4. Rights. Duties and Liabilities of the Contractor </u> <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 4.1 The Contractor has duty to provide the Driver to the Employer on the date and time specifed under Clause
            2 or other date/time as the Employer reasonably notifies the Contractor in advance, in the event of the service outside
            the regular service hour. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 4.2 The Contractor shall be responsible for the expense on salary, travel, accommodation and other beneft of the
            Driver. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 4.3 The Contractor shall be liable for any damages intentionally or negligently caused by the Driver to the
            properties or document of the Employer or the third person. The liability of the Contractor is based on the actual
            damage but shall not be exceeding 10,000 Baht (Ten thousand Baht) per occasion. The Employer has duty to prove
            that such damage is arisen from the act of the Contractor's Driver. <br>
            The Contractor reserves the right not to be liable for any damages specifed above due to the gross negligence
            of the Employer or its employee or the damage incurred to cash, cash cheque, bearer's cheque, antique object, sacred
            object, gems and vehicle. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 4.4 The Contractor shall provide training to the Driver up to the level of driver standard. If the Contractor wishes
            to send the Driver to attend any training, the Contractor shall notify the Employer in writing not less than 7 (seven) days
            in advance. The Employer shall allow the Driver to attend the training as requested by the Contractor. The Contractor
            shall provide temporary driver to replace such driver and still be fully responsible to the Employer as if such replaced
            driver is the Driver under this Agreement. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 4.5 The Contractor shall evaluate the performance of the Driver by collecting the evaluation information of
            the Driver done by the Employer according to the duration provided. The Contractor shall notify the result of such
            evaluation to the Employer. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 4.6 In the event that the Contractor is unable to provide the Driver to the Employer, the Contractor shall notify
            the Employer and provide the temporary Driver to replace such Driver to the Employer. If the Contractor is unable to
            provide the substituted Driver to the Employer, the Contractor agrees that the Employer shall be entitled not to pay the
            service fee for that day by deducting from the service fee of that month but the employer shall notify the Contractor in
            writing for every deductions. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <u> Clause 5. Rights" Duties and Libabilitaes of the Employer </u> <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5.1 The Employer has the duty to fully pay the service fee and/or other amount specifed under Clause 3
            throughout the term of this Agreement. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5.2 The Employer shall not allow or consent the third person to assume the beneft on the service under this
            Agreement whatsoever, unless the written consent is obtained from the Contractor or it is specifed otherwise in this
            Agreement. In the event that the Employer fails to comply with this Clause, the Contractor shall not be liable for any
            damage or loss which might incur to the Employer and the third person in whatever circumstance. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5.3 The Employer shall continually provide frst-class insurance and other insurance under the law regarding the
            car injuries throughout the term of this Agreement by covering the car that is assigned to the Driver. If the Employer fails
            to provide the insurance or the insurance expires before or during the term of this Agreement, the Contractor shall not
            be liable for any damage which might incur from the use of such car, either to the Employer or the third person. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5.4 The Employer shall not allow the Driver to use the car for personal matters or beyond the service day/time
            specifed under Clause 2. The Employer shall be responsible for any expense and / or damage which might incur from
            the car if the Employer allows the Driver to use the car beyond the service day/time specified. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5.5 Within 3 months from the expiry of this Agreement and /or from the date that the Driver is no longer the
            employee of the Contractor, the Employer shall not employ such Driver as its own employee. If the Employer fails to
            comply with this Clause, the Employer is liable to pay the compensation to the Contractor in the amount equivalent to
            3(three) times of the monthly service fee for one Driver. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5.6 The Employer shall be entitled to change the Driver by notifying the Contractor in writing in advance at least
            7 (seven) working days of the Contractor and without any expense, This Clause applies oniy when the Driver fails to
            perform his duty or inappropriately behaves, as follows: <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (1) seriously misbehave. The Contractor shall be person who considers if such behavior is considered serious or
            not; <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (2) fails to comply with any rule or regulation determined by the Employer and the Employer already notifed the
            Contractor to remedy such incompliance twice, except for a serious failure; <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (3) misconducts or intentionally commits criminal offence against the Employer and/or its employee; <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (4) Intentionally causes damage to the Employer; <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <u> Clause 6. Termination of Agreement </u> <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 6.1 This Agreement shall be terminated upon the occurrence of the following events: <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (1) The duration specified under Clause 1 expires; <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (2) The Employer terminates this Agreement under Clause 6.2; <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (3) The Contractor terminates this Agreement under Clause 6.3 and/or 6.4. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 6.2 If the Employer wishes to terminate this Agreement before the expiry of the term under Clause 1, the
            Employer shall notify the Contractor in writing at least 30 (thirty) days in advance. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 6.3 The Contractor may immediately terminate this Agreement without notifying the Employer in advance if the
            Employer fails to pay the service fee or other amount provided under this Agreement for more than 2 months. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 6.4 The Contractor may terminate this Agreement if there is occurrence of the following events and the Employer
            is unable to remedy or comply with the Agreement within 30 (thirty) days after the date that the Employer is notifed or becomes known of such default: <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (1) The Employer fails to pay service fee or fails to pay full service fee or other amount which the Employer is
            obliged to pay the Contractor under this Agreement. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (2) The Employer breached any provision in this Agreement regardless of whether or not such provision is
            material. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (3) The Employer closes its business, fles the application for winding up, liquidation, merging with other person's
            business, except such merger is approved by the Contractor in writing, or terminating the status of juristic person from
            other reason. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (4) The Employer is insolvent, reorganized, bankrupted or placed under the absolute control of receivership. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <u> Clause 7. Consequence of the Termination </u> <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 7.1 In the event that the Agreement terminates as provided under Clause 6.1, the Contractor shall still be
            entitled to demand the Employer to pay the service fee, expense and interest as specifed in this Agreement and/or
            under the laws and/or any other unpaid amount under this Agreement. The termination shall not prejudice the right of
            the Contractor to claim for other damages caused by the Employer under this Agreement and laws. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 7.2 In the event that the Employer terminates the Agreement under Clause 6.2 or the Contractor terminates the
            Agreement under Clause 6.3 and/or 6.4, the Employer shall be liable to pay the compensation, excluding the VAT (if
            any), in the amount equivalent to 3 months of monthly service fee providled in Clause 3. This shall not prejudice the right
            of the Contractor to claim for other compensation or expense caused by the Employer under this agreement and laws. <br>
